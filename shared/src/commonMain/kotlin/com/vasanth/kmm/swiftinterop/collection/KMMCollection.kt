package com.vasanth.kmm.swiftinterop.collection

/**
 * KMM Collection - Used to understand Swift interop with KMM for "List, Set, Map"
 */

// region LIST
/**
 * Take and return List.
 *
 * @param list List.
 *
 * @return List.
 */
fun takeAndReturnList(list: List<String>): List<String> {
    return list
}
// endregion


// region SET
/**
 * Take and return Set.
 *
 * @param set Set.
 *
 * @return Set.
 */
fun takeAndReturnSet(set: Set<String>): Set<String> {
    return set
}
// endregion


// region MAP
/**
 * Take and return Map.
 *
 * @param map Map.
 *
 * @return Map.
 */
fun takeAndReturnMap(map: Map<String, String>): Map<String, String> {
    return map
}
// endregion


